package com.kishore.bikeshop.collection.main;
public class Login 
{
	private String un;
	private String pwd;
	private String role;
	public void setUn(String un)
	{
		this.un=un;	
	}
	public void setPwd(String pwd)
	{
		this.pwd=pwd;
	}
	public void setRole(String role)
	{
		this.role=role;	
	}
	public String getUn()
	{
		return this.un;
	}
	public String getPwd()
	{
		return this.pwd;
	}
	public String getRole()
	{
		return this.role;
	}

}

